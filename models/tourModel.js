tourSchema.pre(/^find/, function(next) {
  ///^find/ ) that means it run for all query with start find
  this.find({
    //here this target current processing query not docs
    secretTour: {
      $ne: true
    }
  });
  this.start = Date.now();
  //execute after run query
  next();
});

tourSchema.post(/^find/, function(docs, next) {
  //execute before run query
  console.log(`Query took ${Date.now() - this.start} milliseconds!`);
  //it log total time of run query
  next();
});
const Tour = mongoose.model('Tour', tourSchema);

module.exports = Tour;
